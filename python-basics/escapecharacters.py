# Escape characters in Strings are used to represent special characters within string literals.

# Escape characters are preceded by a backslash (\)
#  to indicate that the following character has a special meaning.

# Common escape characters in Python:
# \n : Newline
# \t : Tab
# \\ : Backslash
# \' : Single Quote
# \" : Double Quote
print("Hello,\nWorld!")  # Newline
print("Hello,\tWorld!")  # Tab  
print("This is a backslash: \\")  # Backslash
print('It\'s a beautiful day!')  # Single Quote